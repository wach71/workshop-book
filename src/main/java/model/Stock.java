package model;

public class Stock {
    private int id;
    private String name;
    private int quantity;

    public int getId() {
        return id;
    }

    public void getId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void getName(String name) {
        this.name = name;
    }

    public int getQuantity() {
        return quantity;
    }

    public void getName(int quantity) {
        this.quantity = quantity;
    }
}
